## go through the top of the file and get a few things.
my ($pattern, $pattern_name,$seq_file,$pat_seq);
while (<$ifh>) {
    if (/-pattern\s+(\S+)/) {
        $pat_seq = $1;
    } elsif (/pname\s+(\S+)/) {
        $pattern_name = $1;
	  }elsif (/-sequence\s+(\S+)/) {
          $seq_file = $1;
	  }
    elsif(/Report_file/) {
        last;
    }
}

my $pattern = $doc->createAndAddSequence($pattern_name, $pattern_name, undef, 'na', 'DNA');
$doc->createAndAddSeqData($pattern_name,$pat_seq);
## fetch deflines
#my $deflines = get_deflines($sequence_file);

## check that these were successfully parsed
unless ($pattern_name)      { $logger->logdie("HMM file definition not found in input file.") }
unless ($seq_file) { $logger->logdie("Sequence file definition not found in input file.") }

#my $hmm_name = $hmm_file;
#$hmm_name =~ s/.*\///;
#$nmm_name =~ s/\..*//;
my($seq_name,$start,$end,$score,$strand,$pattern, $seq, %alignments);

while (<$ifh>) {
  #Feature: 1
		  #Name: ntnm01.assembly.81.0
		  #Start: 9340
		  #End: 9359
		  #Length: 20
		  #Score: 20
		  #Strand: +
		  #Pattern_name: dRS3_repeat
		  #Mismatch: .

		  if(/Name:\s(\S+)/) {
			$seq_name=$1;
		  }
		  elsif(/Start:\s(\S+)/) {
			$start=$1;
		  }
		  elsif(/End:\s(\S+)/) {
			$end = $1;
		  }
		  elsif(/Strand:\s(\S+)/) {
			$strand=$1;
		  }
		  elsif(/Score:\s(\S+)/) {
			$score=$1;
		  }
		  elsif(/Pattern_name:\s(\S+)/) {
#			$pattern=$1;
			## add this model sequence if we haven't already
			if( !( $doc->returnBsmlSequenceByIDR($seq_name)) ){
                $seq = $doc->createAndAddSequence($seq_name, $seq_name, undef, 'na', 'DNA');
			}
            
            if(!$alignments{$pattern_name}) {
                $alignments{$pattern_name} = $doc->createAndAddSequencePairAlignment( 
                                                                                      refseq => $seq_name,
                                                                                      #refstart => 0,
                                                                                      #refend => $cols[2] - 1,
                                                                                      #reflength => $cols[2],
                                                                                      compseq => $pattern_name,
                                                                                      class => 'match'
                                                                                      );
            }

                ## add a link element inside this seq-pair-alignment
                $alignments{$pattern_name}->addBsmlLink('analysis', "\#$options{search_method}_analysis", 'computed_by');
        
                ## add the total_score and total_eval for this pair
#                $doc->createAndAddBsmlAttribute($alignments{$pattern_name}, 'total_score', $score);
        
			my $linectr=0;
          my $run = $doc->createAndAddSequencePairRun(   
                                                         alignment_pair => $alignments{$pattern_name},
                                                         runscore => $score,
                                                         runlength => abs($start - $end) + 1,
                                                         comprunlength => abs($start - $end) + 1,
